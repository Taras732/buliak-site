$created = 0; $updated = 0;
foreach ( $P as $row ) {
	list( $sku, $name, $cat, $price, $weight, $sklad, $marinade, $opys, $is_best ) = $row;

	$short  = '<p><strong>Ціна:</strong> ' . $price . ' грн/кг';
	if ( $weight !== '—' ) { $short .= '<br><strong>Орієнтовна вага:</strong> ' . $weight; }
	$short .= '<br><strong>Склад:</strong> ' . $sklad;
	if ( $marinade !== '' ) { $short .= '<br><strong>Маринад:</strong> ' . $marinade; }
	$short .= '</p>';

	$existing_id = wc_get_product_id_by_sku( $sku );
	$product = $existing_id ? wc_get_product( $existing_id ) : new WC_Product_Simple();

	$product->set_name( $name );
	$product->set_sku( $sku );
	$product->set_status( 'publish' );
	$product->set_catalog_visibility( 'visible' );
	$product->set_regular_price( (string) $price );
	$product->set_description( '<p>' . $opys . '</p>' );
	$product->set_short_description( $short );
	$product->set_category_ids( array( $cat_ids[ $cat ] ) );
	if ( $is_best && $best_id ) { $product->set_tag_ids( array( $best_id ) ); }
	$product->save();

	if ( $existing_id ) { $updated++; WP_CLI::log( "ОНОВЛЕНО $sku — $name" ); }
	else { $created++; WP_CLI::log( "СТВОРЕНО $sku — $name" ); }
}

