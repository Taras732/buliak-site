$cat_ids = array();
foreach ( $cat_defs as $key => $name ) {
	$term = get_term_by( 'name', $name, 'product_cat' );
	if ( ! $term ) {
		$res = wp_insert_term( $name, 'product_cat' );
		$cat_ids[ $key ] = is_wp_error( $res ) ? 0 : (int) $res['term_id'];
